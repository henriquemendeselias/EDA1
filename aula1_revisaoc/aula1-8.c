#include <stdio.h>

double a, b, c, d, e, f;

void soma (){

    scanf("%lf", &a);
    scanf("%lf", &b);
    double sum = a + b;

    printf("A soma dos dois numeros eh: %lf\n", sum); 
}

void produto (){

    scanf("%lf", &c);
    scanf("%lf", &d);
    double prod = c * d;

    printf("O produto dos dois numeros eh: %lf\n", prod); 
}

void quociente (){

    scanf("%lf", &e);
    scanf("%lf", &f);
    double quo = e / f;

    printf("O quociente dos dois numeros eh: %lf\n", quo); 
}


int main(){
    soma();
    produto();
    quociente();
    return 0;
}



